package controlflow;

public class IfStatements {

    public static void main(String[] args) {
        int x = 3;
        int y = 2;

        if (x > y) {
            System.out.println("x is bigger");
        }

        if (x < y) {
            System.out.println("x is smaller");
        }

        if (x == y) {
            System.out.println("x and y have the same value");
        }
    }

}
